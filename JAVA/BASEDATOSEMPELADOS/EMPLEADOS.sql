
USE ADD_EJEMPLO;

DROP TABLE IF EXISTS EMPLEADO,DEPARTAMENTO;


CREATE TABLE DEPARTAMENTO(
dept_no TINYINT(2) PRIMARY KEY NOT NULL,
dnombre VARCHAR(15) NOT NULL,
loc VARCHAR(15) NOT NULL
);

CREATE TABLE EMPLEADO(
emp_no SMALLINT(4) PRIMARY KEY NOT NULL,
apellido VARCHAR(10),
oficio VARCHAR(10) NOT NULL,
director SMALLINT(4) NOT NULL,
fecha_alta DATE DEFAULT(current_date()),
salario FLOAT(6,2 ) NOT NULL,
comision FLOAT(6,2) DEFAULT 0,
dept_no TINYINT(2) NOT NULL,
CONSTRAINT fk_depto FOREIGN KEY(dept_no) REFERENCES DEPARTAMENTO(dept_no),
CONSTRAINT fk_dire FOREIGN KEY (director) REFERENCES EMPLEADO(emp_no)
);




